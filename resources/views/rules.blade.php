<p><strong>Managing an order</strong></p>

<ul>
	<li>If you are unsure what a given service type means to make sure to check it out on the homepage for more information or ask the admins about it.</li>
	<li>We expect you to play on your active orders on a daily basis. This means at least 4-8 games per day on each processing order. Priority orders are expected to progress at a higher rate. If you don’t play on your orders consistently every day, it is considered to be stalling the order, and will be penalized.</li>
	<li>If you don’t have time to play on your orders, or not for all of them, you are expected to lock out the ones you don’t have sufficient time for.</li>
	<li>Priority orders must be started within an hour after lockin. Normal orders must be started within ~6 hours, or at latest by the end of the day. You mustn’t lock in an order, which you wouldn’t start till the next day!</li>
	<li><strong>Deadlines</strong>: Most soloQ orders have a given time, which determines the latest time you have to finish the order. If you can’t finish the order in the time you don’t get paid for it by default, and the customer may receive compensation for the delay.<br>
	Even if the customer is fine with the delay we still might lower your share from orders that are finished past the deadline.</li>
	<li>If you <strong>lock out an order with relatively little time</strong> left on the order timer, you’ll also have to pay for the time wasted. Please always inform the admins if you pick up an order which only has little time left on it because someone else wasted it before, <strong>we are only able to help you if you contact us as soon as you pick it up</strong>.</li>
	<li>We do <strong>not</strong> provide a <strong>deadline</strong> for most of the <strong>duoQ orders</strong>, still, you are not allowed to keep the customer waiting too long / waste his time. IF you are not able to play with him within ~1 day because of your availability or scheduling issues, you are supposed to <strong>lock out</strong> the order or assign it to another booster.</li>
	<li>We have zero-tolerance for boosters taking our customers and offering them services outside the site. This is called private <strong>boosting/stealing customers</strong> and <strong>will lead to a permanent ban</strong> on the first offense.</li>
	<li>Locking out orders means that&nbsp;you’ll be only able to pick up fewer orders at a time temporarily.</li>
	<li>You can now <strong>assign orders to another booster</strong>. You can ask for their assigned token (can be found in Profile-&gt;Employee profile) on skype, once you've got that in the order you will see the assigned order option. Click on that and fill in the number you got from the other booster and continue.</li>
	<li>Boosters <strong>without processing duoq</strong> orders have to <strong>wait</strong> before locking in a new <strong>soloQ</strong> order. If your duoq order is older than 10-14 days then it is the same as not having a duoq order.</li>
	<li>We guarantee <strong>80% wins of the placement matches</strong> (from Master and above, 70%), rounded up from 0.5 (You can check it by clicking here). If you lose more than the guaranteed, you have to win +2 for every loss or play until the next division.</li>
